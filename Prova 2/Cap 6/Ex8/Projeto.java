public abstract class Projeto {
    private int numero;
    private String nome;
    
    public int getNum(){
        return this.numero;
    }
    public void setNum(int n){
        this.numero=n;
    }
    
    public String getNome(){
        return this.nome;
    }
    public void setNome(String no){
        this.nome=no;
    }
}
